const escapeRegExp = (value: string): string => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const hasRuntimeLiteral = (content: string, literal: string): boolean => {
  const escaped = escapeRegExp(literal);
  const quotedLiteral = new RegExp(`['"\`]${escaped}['"\`]`);
  const jsxTextLiteral = new RegExp(`>\\s*${escaped}\\s*<`);

  return quotedLiteral.test(content) || jsxTextLiteral.test(content);
};

